export function generateFMEATemplate(doc: MHDLDocument, lang?: MedicalLanguage): BuildArtifact {
  const comps = componentList(doc);
  const rows: string[] = [];

  rows.push(
    `# DISCLAIMER: This FMEA was auto-generated by MeshCue Forge as a design aid and starting point. It does NOT constitute regulatory approval or clinical validation. Review by qualified professionals is required.`,
  );
  rows.push(
    `Component,Failure Mode,Effect,Severity (1-10),Occurrence (1-10),Detection (1-10),RPN,Recommended Action`,
  );

  for (const comp of comps) {
    const cat = fmeaCategory(comp);
    const templates = FAILURE_MODES[cat];
    if (!templates) continue;

    for (const t of templates) {
      const rpn = t.severity * t.occurrence * t.detection;
      rows.push(
        `${comp.id},${t.failureMode},${t.effect},${t.severity},${t.occurrence},${t.detection},${rpn},${t.action}`,
      );
    }
  }

  return {
    stage: "docs",
    filename: "FMEA.csv",
    content: rows.join("\n"),
    format: "csv",
  };
}

