/**
 * Medical regulatory document generators for MeshCue Forge.
 *
 * Generates WHO checklists, IEC 62304, FMEA, CE guidance,
 * and battery-life estimation documents for healthcare devices
 * targeting underdeveloped countries.
 */

import type { MHDLDocument, BuildArtifact, Component } from "../../schema/mhdl.js";
import { t, type MedicalLanguage } from "../../i18n/medical.js";

// ─── Medical Disclaimer ─────────────────────────────────────

const MEDICAL_DISCLAIMER = `> **⚠️ Disclaimer:** This document was auto-generated by MeshCue Forge as a design aid
> and starting point. It does NOT constitute regulatory approval, medical device
> certification, or clinical validation. All generated content must be reviewed by
> qualified regulatory and clinical professionals before submission to any regulatory
> body or use in patient care. MeshCue assumes no liability for clinical outcomes.`;

// ─── Helpers ────────────────────────────────────────────────

function deviceName(doc: MHDLDocument): string {
  return doc.meta.name;
}

function componentList(doc: MHDLDocument): Component[] {
  return [doc.board.mcu, ...doc.board.components];
}

function powerVoltage(doc: MHDLDocument): string {
  return `${doc.board.power.voltageIn}V`;
}

function regulatedVoltage(doc: MHDLDocument): string {
  return doc.board.power.regulatorOut
    ? `${doc.board.power.regulatorOut}V`
    : "N/A";
}

function maxCurrent(doc: MHDLDocument): string {
  return `${doc.board.power.maxCurrentMa}mA`;
}

function ipRating(doc: MHDLDocument): string {
  const enc = doc.enclosure;
  if (enc.ventilation) return "IP20 (ventilated enclosure)";
  const hasCutouts = enc.cutouts.length > 0;
  return hasCutouts ? "IP40 (cutouts present)" : "IP54 (sealed enclosure)";
}

function enclosureMaterial(doc: MHDLDocument): string {
  return (doc.enclosure.material || "pla").toUpperCase();
}

function mcuDescription(doc: MHDLDocument): string {
  const mcu = doc.board.mcu;
  return `${mcu.model || mcu.family} (${mcu.family})`;
}

/** Classify a component into a broad FMEA category. */
function fmeaCategory(comp: Component): string {
  const t = comp.type;
  if (t === "mcu") return "MCU";
  if (["led", "oled", "lcd", "neopixel"].includes(t)) return "Display";
  if (["button", "joystick", "potentiometer", "encoder"].includes(t)) return "Button";
  if (["buzzer", "speaker"].includes(t)) return "Buzzer";
  if (t === "voltage-regulator") return "Power";
  if (
    [
      "sensor",
      "temperature_sensor",
      "thermocouple",
      "moisture",
      "gas_sensor",
      "ldr",
      "pir",
      "ultrasonic",
      "gps",
      "rfid",
    ].includes(t)
  )
    return "Sensor";
  if (["connector", "antenna"].includes(t)) return "Connection";
  return "Connection"; // default bucket
}

