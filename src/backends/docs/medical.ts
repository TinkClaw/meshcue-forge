// ─── 2. IEC 62304 ───────────────────────────────────────────

export function generateIEC62304Doc(doc: MHDLDocument, lang?: MedicalLanguage): BuildArtifact {
  const s = t(lang);
  const comps = componentList(doc);
  const mcu = doc.board.mcu;
  const fw = doc.firmware;
  const lines: string[] = [];

  lines.push(MEDICAL_DISCLAIMER);
  lines.push(``);
  lines.push(`# ${s.iec62304Title}`);
  lines.push(`## ${deviceName(doc)}`);
  lines.push(``);
  lines.push(`> ${s.autoGenerated}.`);
  lines.push(``);

  // Software System Overview
  lines.push(`## 1. Software System Overview`);
  lines.push(``);
  lines.push(`- **Device**: ${deviceName(doc)}`);
  lines.push(`- **Description**: ${doc.meta.description}`);
  lines.push(`- **Firmware framework**: ${fw.framework} (auto)`);
  lines.push(`- **Entry point**: ${fw.entrypoint} (auto)`);
  lines.push(`- **MCU family**: ${mcu.family} (auto)`);
  lines.push(`- **MCU model**: ${mcu.model || mcu.family} (auto)`);
  if (mcu.clockMhz) lines.push(`- **Clock**: ${mcu.clockMhz} MHz (auto)`);
  if (mcu.flashKb) lines.push(`- **Flash**: ${mcu.flashKb} KB (auto)`);
  if (mcu.ramKb) lines.push(`- **RAM**: ${mcu.ramKb} KB (auto)`);
  if (mcu.wireless?.length) lines.push(`- **Wireless**: ${mcu.wireless.join(", ")} (auto)`);
  lines.push(``);
  lines.push(`### Software purpose`);
  lines.push(`<!-- Describe the clinical purpose of the software -->`);
  lines.push(``);

  // Software Architecture
  lines.push(`## 2. Software Architecture (auto-generated)`);
  lines.push(``);
  lines.push(`\`\`\``);
  lines.push(`┌─────────────────────────────────────┐`);
  lines.push(`│           Application Layer          │`);
  lines.push(`│  (${fw.entrypoint})${" ".repeat(Math.max(0, 30 - fw.entrypoint.length))}│`);
  lines.push(`├─────────────────────────────────────┤`);
  lines.push(`│           Library Layer              │`);
  for (const lib of fw.libraries) {
    const label = `│  ${lib.name}${lib.version ? `@${lib.version}` : ""}`;
    lines.push(`${label}${" ".repeat(Math.max(0, 38 - label.length))}│`);
  }
  lines.push(`├─────────────────────────────────────┤`);
  lines.push(`│        HAL / Framework Layer         │`);
  lines.push(`│  (${fw.framework})${" ".repeat(Math.max(0, 30 - fw.framework.length))}│`);
  lines.push(`├─────────────────────────────────────┤`);
  lines.push(`│           Hardware (MCU)             │`);
  lines.push(`│  ${mcu.model || mcu.family}${" ".repeat(Math.max(0, 34 - (mcu.model || mcu.family).length))}│`);
  lines.push(`└─────────────────────────────────────┘`);
  lines.push(`\`\`\``);
  lines.push(``);

  // Software Items
  lines.push(`## 3. Software Items (auto-generated)`);
  lines.push(``);
  lines.push(`Each component below maps to a firmware module or driver.`);
  lines.push(``);
  lines.push(`| # | Component | Type | GPIO Pins | Firmware Module |`);
  lines.push(`|---|-----------|------|-----------|-----------------|`);
  comps.forEach((c, i) => {
    const gpios = c.pins
      .filter((p) => p.gpio !== undefined)
      .map((p) => `GPIO${p.gpio}`)
      .join(", ");
    const module = c.type === "mcu" ? "core/hal" : `drivers/${c.type}`;
    lines.push(`| ${i + 1} | ${c.id} | ${c.type} | ${gpios || "—"} | ${module} |`);
  });
  lines.push(``);
  lines.push(`Total component count: **${comps.length}** (auto)`);
  lines.push(``);

  // Risk Classification
  lines.push(`## 4. Risk Classification`);
  lines.push(``);
  lines.push(`Per IEC 62304, software is classified into three safety classes:`);
  lines.push(``);
  lines.push(`| Class | Description | Applies? |`);
  lines.push(`|-------|-------------|----------|`);
  lines.push(`| **A** | No injury or damage to health possible | Most likely for this device |`);
  lines.push(`| **B** | Non-serious injury possible | Review if device provides clinical measurements |`);
  lines.push(`| **C** | Death or serious injury possible | Unlikely for Forge-class devices |`);
  lines.push(``);
  lines.push(`> **Recommendation**: Most MeshCue Forge devices fall under **Class A**`);
  lines.push(`> (low risk). If the device contributes to clinical decisions, reassess as Class B.`);
  lines.push(``);

  // Software Testing
  lines.push(`## 5. Software Testing`);
  lines.push(``);
  lines.push(`### 5.1 Unit Tests`);
  lines.push(`<!-- List unit tests for each software module -->`);
  lines.push(``);
  lines.push(`### 5.2 Integration Tests`);
  lines.push(`<!-- List integration tests verifying component interactions -->`);
  lines.push(``);
  lines.push(`### 5.3 System Tests`);
  lines.push(`<!-- List end-to-end tests for the complete firmware -->`);
  lines.push(``);
  lines.push(`### 5.4 Test reference`);
  lines.push(`- Framework: ${fw.framework}`);
  if (fw.features?.length) {
    lines.push(`- Features under test: ${fw.features.join(", ")}`);
  }
  lines.push(``);

  // Software Maintenance Plan
  lines.push(`## 6. Software Maintenance Plan`);
  lines.push(``);
  lines.push(`### 6.1 Firmware update mechanism`);
  lines.push(`<!-- Describe OTA or physical update process -->`);
  lines.push(``);
  lines.push(`### 6.2 Version control`);
  lines.push(`- Current version: ${doc.meta.version}`);
  lines.push(`- Schema version: ${doc.meta.schemaVersion}`);
  lines.push(``);
  lines.push(`### 6.3 Problem resolution process`);
  lines.push(`<!-- Describe bug reporting and fix process -->`);
  lines.push(``);

  // Class A simplified guidance
  lines.push(`## Appendix: Simplified Class A Guidance`);
  lines.push(``);
  lines.push(`For Class A software (no risk of injury), IEC 62304 requires:`);
  lines.push(``);
  lines.push(`1. **Software development planning** — this document`);
  lines.push(`2. **Software requirements** — derived from device requirements`);
  lines.push(`3. **Software architecture** — Section 2 above`);
  lines.push(`4. **Software unit verification** — basic testing of each module`);
  lines.push(`5. **Software release** — documented release with version number`);
  lines.push(`6. **Software maintenance** — Section 6 above`);
  lines.push(``);
  lines.push(`Class A does NOT require:`);
  lines.push(`- Detailed design documentation`);
  lines.push(`- Software integration testing (recommended but not mandatory)`);
  lines.push(`- Formal code reviews (recommended but not mandatory)`);

  return {
    stage: "docs",
    filename: "IEC62304_SOFTWARE_LIFECYCLE.md",
    content: lines.join("\n"),
    format: "markdown",
  };
}

