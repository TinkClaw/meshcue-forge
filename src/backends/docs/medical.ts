// ─── 5. Battery Life Estimate ───────────────────────────────

interface BatteryOption {
  name: string;
  capacityMah: number;
  voltage: number;
}

const BATTERY_OPTIONS: BatteryOption[] = [
  { name: "18650 Li-Ion", capacityMah: 3400, voltage: 3.7 },
  { name: "2x AA Alkaline", capacityMah: 2500, voltage: 3.0 },
  { name: "CR2032 Coin Cell", capacityMah: 220, voltage: 3.0 },
  { name: "LiPo 1000mAh", capacityMah: 1000, voltage: 3.7 },
  { name: "LiPo 2000mAh", capacityMah: 2000, voltage: 3.7 },
];

/** Rough current draw by component type (mA, active state) */
function estimateComponentCurrentMa(comp: Component): number {
  const t = comp.type;
  if (t === "mcu") return 80;
  if (t === "oled") return 20;
  if (t === "lcd") return 25;
  if (t === "led" || t === "neopixel") return 20;
  if (t === "buzzer" || t === "speaker") return 30;
  if (["sensor", "temperature_sensor", "thermocouple", "moisture", "gas_sensor", "ldr", "pir"].includes(t))
    return 5;
  if (t === "ultrasonic") return 15;
  if (t === "gps") return 50;
  if (t === "rfid") return 30;
  if (["motor", "servo", "stepper"].includes(t)) return 200;
  if (t === "relay") return 70;
  return 5; // default for passive or low-power components
}

export function generateBatteryLifeEstimate(doc: MHDLDocument, lang?: MedicalLanguage): BuildArtifact {
  const comps = componentList(doc);
  const mcu = doc.board.mcu;
  const lines: string[] = [];

  // Calculate current draws
  const currentBreakdown: { name: string; activeMa: number; dutyPct: number; avgMa: number }[] = [];

  for (const comp of comps) {
    const activeMa = estimateComponentCurrentMa(comp);
    let dutyPct: number;

    // Assign duty cycles based on type
    if (comp.type === "mcu") {
      dutyPct = 100; // always on
    } else if (
      ["sensor", "temperature_sensor", "thermocouple", "moisture", "gas_sensor", "ldr", "pir", "ultrasonic", "gps", "rfid"].includes(
        comp.type,
      )
    ) {
      dutyPct = 10; // reads every ~10s, active ~1s
    } else if (["oled", "lcd", "led", "neopixel"].includes(comp.type)) {
      dutyPct = 50; // display on 50% of time
    } else if (["buzzer", "speaker"].includes(comp.type)) {
      dutyPct = 2; // alarm rarely active
    } else if (["motor", "servo", "stepper"].includes(comp.type)) {
      dutyPct = 5; // intermittent actuation
    } else {
      dutyPct = 10;
    }

    const avgMa = activeMa * (dutyPct / 100);
    currentBreakdown.push({ name: comp.id, activeMa, dutyPct, avgMa });
  }

  // WiFi burst estimate
  const hasWifi = mcu.wireless?.includes("wifi");
  const hasBle = mcu.wireless?.includes("ble") || mcu.wireless?.includes("bluetooth");
  if (hasWifi) {
    currentBreakdown.push({ name: "WiFi (burst)", activeMa: 170, dutyPct: 5, avgMa: 8.5 });
  }
  if (hasBle) {
    currentBreakdown.push({ name: "BLE (advertising)", activeMa: 15, dutyPct: 10, avgMa: 1.5 });
  }

  const totalAvgMa = currentBreakdown.reduce((sum, c) => sum + c.avgMa, 0);

  const s = t(lang);

  lines.push(MEDICAL_DISCLAIMER);
  lines.push(``);
  lines.push(`# ${s.batteryLifeTitle}`);
  lines.push(`## ${deviceName(doc)}`);
  lines.push(``);
  lines.push(`> ${s.autoGenerated}.`);
  lines.push(``);

  // Current draw breakdown
  lines.push(`## 1. ${s.currentDrawBreakdown}`);
  lines.push(``);
  lines.push(`| ${s.component} | Active (mA) | Duty Cycle | Average (mA) |`);
  lines.push(`|-----------|-------------|------------|--------------|`);
  for (const c of currentBreakdown) {
    lines.push(`| ${c.name} | ${c.activeMa} | ${c.dutyPct}% | ${c.avgMa.toFixed(1)} |`);
  }
  lines.push(`| **Total** | | | **${totalAvgMa.toFixed(1)}** |`);
  lines.push(``);

  // Duty cycle assumptions
  lines.push(`## 2. ${s.dutyCycleAssumptions}`);
  lines.push(``);
  lines.push(`- Sensor reads: every 10 seconds (1s active, 9s sleep)`);
  lines.push(`- Display: on 50% of the time (user interaction or continuous readout)`);
  lines.push(`- Buzzer/alarm: active 2% of the time (periodic alerts)`);
  if (hasWifi) lines.push(`- WiFi: burst every 5 minutes, ~3s per burst (5% duty)`);
  if (hasBle) lines.push(`- BLE: advertising at 10% duty cycle`);
  lines.push(`- MCU: always on (no deep sleep assumed)`);
  lines.push(``);

  // Battery life estimates
  lines.push(`## 3. ${s.estimatedBatteryLife}`);
  lines.push(``);
  lines.push(`| Battery | Capacity | Voltage | Est. Hours | Est. Days |`);
  lines.push(`|---------|----------|---------|------------|-----------|`);

  let recommendedBattery = BATTERY_OPTIONS[0];
  let bestScore = 0;

  for (const bat of BATTERY_OPTIONS) {
    const hours = bat.capacityMah / totalAvgMa;
    const days = hours / 24;
    lines.push(
      `| ${bat.name} | ${bat.capacityMah} mAh | ${bat.voltage}V | ${hours.toFixed(1)} | ${days.toFixed(1)} |`,
    );

    // Scoring: prefer 2+ days of runtime, penalise overkill
    const dayScore = Math.min(days, 30) / 30;
    if (dayScore > bestScore) {
      bestScore = dayScore;
      recommendedBattery = bat;
    }
  }
  lines.push(``);

  // Recommendation
  lines.push(`## 4. ${s.recommendation}`);
  lines.push(``);
  const recHours = recommendedBattery.capacityMah / totalAvgMa;
  const recDays = recHours / 24;
  lines.push(`**Recommended battery**: ${recommendedBattery.name} (${recommendedBattery.capacityMah} mAh)`);
  lines.push(`- Estimated runtime: ${recHours.toFixed(1)} hours (${recDays.toFixed(1)} days)`);
  lines.push(`- Voltage: ${recommendedBattery.voltage}V`);
  lines.push(``);
  if (totalAvgMa < 10) {
    lines.push(`> This device has very low power consumption. A CR2032 coin cell`);
    lines.push(`> may be sufficient for months of operation.`);
  } else if (totalAvgMa < 50) {
    lines.push(`> Moderate power consumption. A single 18650 or LiPo 2000mAh cell`);
    lines.push(`> should provide multiple days of continuous operation.`);
  } else {
    lines.push(`> Relatively high power consumption. Consider a large LiPo or`);
    lines.push(`> 18650 cell, and implement aggressive power management (deep sleep).`);
  }
  lines.push(``);

  // Solar panel sizing
  lines.push(`## 5. ${s.solarPanelSizing}`);
  lines.push(``);
  const avgWatts = (totalAvgMa / 1000) * 3.7; // assume 3.7V system
  const solarHoursPerDay = 4; // conservative for tropical regions
  const panelEfficiency = 0.7; // losses from charge controller, angle, etc.
  const panelWatts = avgWatts / (solarHoursPerDay / 24) / panelEfficiency;
  lines.push(`- Average power consumption: ${(avgWatts * 1000).toFixed(0)} mW`);
  lines.push(`- Assumed peak solar hours: ${solarHoursPerDay} h/day (tropical/subtropical)`);
  lines.push(`- Charge controller efficiency: ${(panelEfficiency * 100).toFixed(0)}%`);
  lines.push(`- **Minimum solar panel**: ${panelWatts.toFixed(2)} W`);
  lines.push(`- **Recommended solar panel**: ${(panelWatts * 1.5).toFixed(2)} W (1.5x margin)`);
  lines.push(``);
  lines.push(`> For reliable outdoor deployment, pair the solar panel with a`);
  lines.push(`> LiPo 2000mAh+ battery to cover cloudy days and nighttime operation.`);

  return {
    stage: "docs",
    filename: "BATTERY_LIFE_ESTIMATE.md",
    content: lines.join("\n"),
    format: "markdown",
  };
}

// ─── 6. Field Test Checklist ────────────────────────────────

export function generateFieldTestChecklist(doc: MHDLDocument, lang?: MedicalLanguage): BuildArtifact {
  const comps = componentList(doc);
  const lines: string[] = [];

  const str = t(lang);

  lines.push(MEDICAL_DISCLAIMER);
  lines.push(``);
  lines.push(`# ${str.fieldTestChecklist}`);
  lines.push(`## ${deviceName(doc)}`);
  lines.push(``);
  lines.push(`> ${str.autoGenerated}.`);
  lines.push(``);

  // ── 1. Functional Testing ──
  lines.push(`## 1. ${str.functionalTesting}`);
  lines.push(``);

  // Sensor checks
  const sensors = comps.filter((c) =>
    [
      "sensor", "temperature_sensor", "thermocouple", "moisture",
      "gas_sensor", "ldr", "pir", "ultrasonic", "gps", "rfid",
    ].includes(c.type),
  );
  if (sensors.length > 0) {
    for (const s of sensors) {
      lines.push(`- [ ] **${s.id}** (${s.type}${s.model ? `, ${s.model}` : ""}): reads within expected range`);
    }
  } else {
    lines.push(`- [ ] All sensor inputs read within expected range`);
  }

  // Display checks
  const displays = comps.filter((c) =>
    ["oled", "lcd", "neopixel", "led"].includes(c.type),
  );
  if (displays.length > 0) {
    for (const d of displays) {
      lines.push(`- [ ] **${d.id}** (${d.type}): displays correct values / status`);
    }
  } else {
    lines.push(`- [ ] Display shows correct values`);
  }

  // Button checks
  const buttons = comps.filter((c) =>
    ["button", "joystick", "potentiometer", "encoder"].includes(c.type),
  );
  if (buttons.length > 0) {
    for (const b of buttons) {
      lines.push(`- [ ] **${b.id}** (${b.type}): responds to user input`);
    }
  } else {
    lines.push(`- [ ] All buttons / inputs respond correctly`);
  }

  // Buzzer / alarm checks
  const buzzers = comps.filter((c) => ["buzzer", "speaker"].includes(c.type));
  if (buzzers.length > 0) {
    for (const bz of buzzers) {
      lines.push(`- [ ] **${bz.id}** (${bz.type}): alarm triggers at threshold`);
    }
  } else {
    lines.push(`- [ ] Buzzer / alarm triggers at threshold (if applicable)`);
  }

  // Connectivity checks
  const mcu = doc.board.mcu;
  if (mcu.wireless?.includes("wifi")) {
    lines.push(`- [ ] WiFi connects to access point and transmits data`);
  }
  if (mcu.wireless?.includes("ble") || mcu.wireless?.includes("bluetooth")) {
    lines.push(`- [ ] BLE advertising is visible and pairing works`);
  }
  lines.push(``);

  // ── 2. Safety Testing ──
  lines.push(`## 2. ${str.safetyTesting}`);
  lines.push(``);
  lines.push(`- [ ] No exposed conductors or solder joints accessible to user`);
  lines.push(`- [ ] Enclosure is sealed (perform IP spray test if rated)`);
  lines.push(`- [ ] No sharp edges on enclosure or components`);
  lines.push(`- [ ] Battery is secure and cannot shift during normal handling`);
  lines.push(`- [ ] No overheating after 1 hour of continuous operation (measure with IR thermometer)`);
  lines.push(`- [ ] Fuse / overcurrent protection verified (if applicable)`);
  lines.push(``);

  // ── 3. Accuracy Testing ──
  lines.push(`## 3. ${str.accuracyTesting}`);
  lines.push(``);
  lines.push(`For each sensor, compare 10 measurements against a reference device and calculate mean error.`);
  lines.push(``);

  if (sensors.length > 0) {
    lines.push(`| Sensor | Reference Device | Measurements | Mean Error | Acceptable Tolerance | Pass? |`);
    lines.push(`|--------|-----------------|--------------|------------|---------------------|-------|`);
    for (const s of sensors) {
      let tolerance = "TBD";
      const t = s.type;
      const model = (s.model || "").toLowerCase();
      if (t === "temperature_sensor" || t === "thermocouple" || model.includes("temp") || model.includes("ds18b20") || model.includes("dht")) {
        tolerance = "±0.3°C";
      } else if (model.includes("max301") || model.includes("spo2") || model.includes("pulse")) {
        tolerance = "±2% SpO2";
      } else if (model.includes("bp") || model.includes("blood") || model.includes("pressure")) {
        tolerance = "±3 mmHg";
      } else if (t === "moisture" || t === "gas_sensor" || t === "ldr") {
        tolerance = "±5%";
      } else if (t === "ultrasonic") {
        tolerance = "±1 cm";
      } else if (t === "gps") {
        tolerance = "±5 m CEP";
      }
      lines.push(`| ${s.id} | | 10 readings | | ${tolerance} | [ ] |`);
    }
  } else {
    lines.push(`| Sensor | Reference Device | Measurements | Mean Error | Acceptable Tolerance | Pass? |`);
    lines.push(`|--------|-----------------|--------------|------------|---------------------|-------|`);
    lines.push(`| (list sensors) | | 10 readings | | TBD | [ ] |`);
  }
  lines.push(``);

  // ── 4. Battery Testing ──
  lines.push(`## 4. ${str.batteryTesting}`);
  lines.push(``);
  lines.push(`- [ ] Measure actual battery life vs. estimated (see BATTERY_LIFE_ESTIMATE.md)`);
  lines.push(`  - Estimated: ______ hours`);
  lines.push(`  - Actual: ______ hours`);
  lines.push(`  - Ratio (actual/estimated): ______`);
  lines.push(`- [ ] Low-battery warning triggers at 20% remaining capacity`);
  lines.push(`- [ ] Graceful shutdown occurs at 5% remaining capacity`);
  lines.push(`- [ ] Device recovers correctly after full battery drain and recharge`);
  lines.push(``);

  // ── 5. Environmental Testing ──
  lines.push(`## 5. ${str.environmentalTesting}`);
  lines.push(``);
  lines.push(`- [ ] Operate at **40°C** for 1 hour — verify stable sensor readings and no overheating`);
  lines.push(`- [ ] Operate at **high humidity** (>80% RH) for 1 hour — verify no condensation on electronics`);
  lines.push(`- [ ] **Drop test**: drop assembled device from 1 m onto concrete`);
  lines.push(`  - [ ] Enclosure intact (no cracks or separation)`);
  lines.push(`  - [ ] Device powers on and functions after drop`);
  lines.push(`  - [ ] No loose internal components after drop`);
  if (doc.enclosure.ipRating) {
    lines.push(`- [ ] **IP rating test (${doc.enclosure.ipRating})**: verify per IEC 60529 requirements`);
  }
  lines.push(``);

  // ── 6. Documentation ──
  lines.push(`## 6. Documentation`);
  lines.push(``);
  lines.push(`- [ ] Serial number assigned: ________________`);
  lines.push(`- [ ] Calibration date recorded: ____/____/________`);
  lines.push(`- [ ] Operator trained on device use`);
  lines.push(`- [ ] Patient information leaflet available`);
  lines.push(`- [ ] Instructions for Use (IFU) included with device`);
  lines.push(`- [ ] Maintenance schedule documented`);
  lines.push(`- [ ] Contact information for technical support provided`);
  lines.push(``);

  // ── Sign-off ──
  lines.push(`## Sign-off`);
  lines.push(``);
  lines.push(`| Role | Name | Signature | Date |`);
  lines.push(`|------|------|-----------|------|`);
  lines.push(`| Tester | | | |`);
  lines.push(`| Reviewer | | | |`);
  lines.push(`| Approver | | | |`);

  return {
    stage: "docs",
    filename: "FIELD_TEST_CHECKLIST.md",
    content: lines.join("\n"),
    format: "markdown",
  };
}
